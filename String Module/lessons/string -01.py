str1 = "Md Abdul Aouwal"
str2 = "MD ABDUL AOUWAL"

print('Title: ',str1.title()) # First Letter of Each word will be capitalized
print('Capitalize: ',str1.capitalize()) # First Letter of Each Sentence will be capitalized
print('Lower Case : ',str2.lower()) # Each Letter  will be in lowercase
print('Upper Case : ',str2.upper()) # Each Letter  will be in uppercase
print('Casefold : ',str2.casefold()) # Each Letter  will be in lowercase
print('SwapCase : ',str1.swapcase()) # Each Letter  will be in lowercase


